import React from 'react'
import {Image, ScrollView, Text, TouchableOpacity, View} from "react-native";
import {fallbackPersonImage, image185} from "../api/MovieDb";
import {ApiMovieCreditsCast} from "../api/response/ApiResponse";
import {NavigationProp} from "@react-navigation/core/src/types";


type CastProps = {
    cast: ApiMovieCreditsCast[];
    navigation: NavigationProp<ReactNavigation.RootParamList>;
};

export const Cast = ({cast, navigation}: CastProps) => {

    return (
        <View style={{marginVertical: 8}}>
            <Text
                style={{
                    color: 'white',
                    fontSize: 18,
                    lineHeight: 24,
                    marginHorizontal: 8
                }}>Top Cast</Text>
            <ScrollView
                horizontal
                showsHorizontalScrollIndicator={false}
                style={{paddingHorizontal: 15, marginTop: 10}}
            >
                {
                    cast && cast.map((person, index) => {
                        return (
                            <TouchableOpacity
                                key={index}
                                // @ts-ignore
                                onPress={() => navigation.navigate('Person', person)}
                                style={{marginRight: 8, flexDirection: 'column', alignItems: 'center'}}
                            >

                                <Image
                                    style={{
                                        overflow: 'hidden',
                                        borderRadius: 1000,
                                        height: 72,
                                        width: 72,
                                        borderWidth: 2,
                                        borderColor: 'rgb(115 115 115)',
                                    }}
                                    source={{uri: image185(person?.profile_path) || fallbackPersonImage}}
                                />

                                <Text className="text-white text-xs mt-1">
                                    {
                                        person?.character.length > 10 ? person.character.slice(0, 10) + '...' : person?.character
                                    }
                                </Text>

                                <Text className="text-neutral-400 text-xs">
                                    {
                                        person?.original_name.length > 10 ? person.original_name.slice(0, 10) + '...' : person?.original_name
                                    }
                                </Text>
                            </TouchableOpacity>
                        )
                    })
                }

            </ScrollView>

        </View>
    )
}

